  SELECT
    USERS.ID AS USER_ID,
    AREAS.ID,
    AREAS.NAME,
    AREAS.DESCRIPTION,
    AREAS.ACTIVE
FROM USERS, AREAS, USER_AREA
WHERE
USERS.ID = USER_AREA.ID_USER
AND
AREAS.ID = USER_AREA.ID_AREA
;
